var app = new Vue({
  el: '#avenge',
  data: {
    bannerImage: 'https://i.redd.it/1njzxa55z7m01.png',
    spanImage: 'https://i.redd.it/1njzxa55z7m01.png',
    heros: heroData,
    searchString: "",
    activeHero: heroData[0] },

  methods: {
    init() {

    },
    focusSearch() {
      $('#search-input').focus();
    },
    toggleFocus() {
      $('.search').toggleClass('focused');
    },
    searchHeros() {
      if (this.searchString.length == 0) {
        $(".hero").css("display", "initial");
      } else {
        $(".hero").css("display", "none");
        var strLow = this.searchString.toLowerCase();
        $(".hero").each(function () {
          var heroName = $(this).
          attr("data-hero-name").
          toLowerCase();
          if (heroName.includes(strLow)) {
            $(this).css("display", "initial");
          }
        });
      }
    },
    selectHero(heroID) {
      this.searchString = '';
      this.activeHero = this.heros[heroID];

      this.spanImage = this.activeHero.bannerImage;

      var timeout = 0;
      $('.transition span').each(function () {
        var self = $(this);
        setTimeout(function () {
          self.addClass('show');
        }, timeout);
        timeout = timeout + 100;
      });

      setTimeout(function () {
        $('.homepage').addClass('close');
        $('.heropage').addClass('open');
      }, 500);


      setTimeout(function () {
        timeout = 0;
        $('.transition span').each(function () {
          var self = $(this);
          setTimeout(function () {
            self.addClass('outDown');
          }, timeout);
          timeout = timeout + 100;
        });
      }, 800);



    },
    backToHome() {

      this.spanImage = this.bannerImage;

      var timeout = 0;
      $('.transition span').each(function () {
        var self = $(this);
        setTimeout(function () {
          self.removeClass('outDown');

        }, timeout);
        timeout = timeout + 100;
      });

      setTimeout(function () {
        $('.homepage').removeClass('close');
        $('.heropage').removeClass('open');
        $('.homepage').scrollTop(0);
      }, 500);


      setTimeout(function () {
        timeout = 0;
        $('.transition span').each(function () {
          var self = $(this);
          setTimeout(function () {
            self.removeClass('show');
          }, timeout);
          timeout = timeout + 100;
        });
      }, 800);









    } },


  watch: {
    searchString: function (val, oldVal) {
      this.searchHeros();
    } },

  mounted() {
    this.init();
  } });